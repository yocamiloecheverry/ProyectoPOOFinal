    private void jButtonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIngresarActionPerformed
        Conexion objConexion = new Conexion();
        objConexion.getConexion();
        String celular = txtCelularL.getText();
        String contrasenia = String.valueOf(txtContraseniaL.getPassword());
        switch (objConexion.verificarUsuario(celular, contrasenia)) {
            case "ADMINISTRADOR":
                FrmAdministrador frmAdministrador = new FrmAdministrador();
                frmAdministrador.setVisible(true);
                break;
            case "CLIENTE":
                FrmAcciones frmAcciones = new FrmAcciones(txtCelularL.getText());
                frmAcciones.setVisible(true);
                break;
            default:
                JOptionPane.showMessageDialog(null, "ERROR, El cliente no existe","",JOptionPane.ERROR_MESSAGE);
                limpiarCampos();
                break;
        }
        objConexion.cerrarConexion();
    }//GEN-LAST:event_jButtonIngresarActionPerformed

